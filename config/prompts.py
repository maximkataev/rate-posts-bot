"""Evaluation prompts for different LLM providers."""

OPENAI_EVALUATION_PROMPT = """Оцени этот пост по 10-балльной шкале.

Критерии оценки:
- Качество контента и информативность
- Вовлечённость и интерес для аудитории
- Ясность изложения
- Визуальная составляющая (если есть)
- Актуальность темы

Пост:
{content}

Ответь ТОЛЬКО числом от 1 до 10 и кратким комментарием (1-2 предложения).
Формат ответа: "Оценка: X/10. Комментарий. Рекомендация ставить лайк или нет"
"""

CLAUDE_EVALUATION_PROMPT = """Проанализируй и оцени этот пост по 10-балльной шкале.

Критерии оценки:
- Качество контента и информативность
- Вовлечённость и интерес для аудитории
- Ясность изложения
- Визуальная составляющая (если есть)
- Актуальность темы

Пост:
{content}

Ты критичный рецензент. Покритикуй обязательно. Можешь занизить оценку.

Предоставь числовую оценку от 1 до 10 и краткое обоснование (1-2 предложения).
Формат ответа: "Оценка: X/10. Комментарий. Рекомендация ставить лайк или нет"
"""

GEMINI_EVALUATION_PROMPT = """Оцени этот пост по 10-балльной шкале.

Критерии оценки:
- Качество контента и информативность
- Вовлечённость и интерес для аудитории
- Ясность изложения
- Визуальная составляющая (если есть)
- Актуальность темы

Пост:
{content}

Ты подбадривающий рецензент. 

Дай числовую оценку от 1 до 10 и краткий комментарий (1-2 предложения).
Формат: "Оценка: X/10. Комментарий. Рекомендация ставить лайк или нет"
"""

# Словарь для удобного доступа
PROMPTS = {
    "openai": OPENAI_EVALUATION_PROMPT,
    "claude": CLAUDE_EVALUATION_PROMPT,
    "gemini": GEMINI_EVALUATION_PROMPT,
}


def get_prompt(llm_name: str) -> str:
    """
    Get evaluation prompt for specific LLM.

    Args:
        llm_name: Name of the LLM (openai, claude, gemini)

    Returns:
        Evaluation prompt template
    """
    return PROMPTS.get(llm_name.lower(), OPENAI_EVALUATION_PROMPT)
